package apuntesdeclase;

public class Variable {



    public static void main(String[] args){

        //Las variables son espacios de memoria que contienen un valor referenciado en la RAM del pc
        String nombre; // Esto es declarar una variable

        nombre = "Maria";


        String apellido = "Iribarne"; // Esto es declarar e inicializar la variable al mismo tiempo

        System.out.println("El nombre es: " + nombre + " "+ apellido); // Esto es concatenar e imprimir una variable



    }
}
