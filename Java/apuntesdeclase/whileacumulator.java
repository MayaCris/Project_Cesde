package apuntesdeclase;

public class whileacumulator {
    public static void main(String[] args) {
        //Imprima los numeros del 1 al 100 usando while
        //Esto es un acumulador

        int num = 0;

        while(num < 100){
            System.out.println(num++); //Es la manera abreviada de escribir < num = num + 1 >

        }
    }
}
